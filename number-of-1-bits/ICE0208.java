class Solution2 {
    /*
     * 시간 복잡도: O(k)
     * - k는 n의 이진 표현에서 1인 비트의 개수이다.
     * - n & (n - 1)을 할 때마다 가장 오른쪽 1 비트가 하나씩 제거되므로,
     *   1의 개수만큼 반복한다.
     * - 문제 조건상 n은 최대 31비트 정수이므로 실질적으로는 O(1)로 볼 수 있다.
     *
     * 공간 복잡도: O(1)
     * - 추가 변수만 사용한다.
     */
    public int hammingWeight(int n) {
        int answer = 0;

        while (n != 0) {
            /*
             * n & (n - 1)은 n의 가장 오른쪽 1 비트를 제거한다.
             *
             * 예를 들어 n = 1100일 때,
             * n - 1 = 1011이 되고,
             *
             *   1100
             * & 1011
             * ------
             *   1000
             *
             * 처럼 가장 오른쪽 1 비트가 제거된다.
             */
            n = n & (n - 1);
            answer++;
        }

        return answer;
    }
}

class Solution1 {
    /*
     * 시간 복잡도: O(b)
     * - b는 n의 이진 표현 길이이다.
     * - 오른쪽 비트부터 하나씩 확인하므로 비트 수만큼 반복한다.
     * - 문제 조건상 n은 최대 31비트 정수이므로 실질적으로는 O(1)로 볼 수 있다.
     *
     * 공간 복잡도: O(1)
     * - 추가 변수만 사용한다.
     */
    public int hammingWeight(int n) {
        int answer = 0;

        while (n != 0) {
            // 가장 오른쪽 비트가 1이면 카운트한다.
            if ((n & 1) == 1) {
                answer++;
            }

            // 다음 비트를 확인하기 위해 오른쪽으로 한 칸 이동한다.
            n >>>= 1;
        }

        return answer;
    }
}

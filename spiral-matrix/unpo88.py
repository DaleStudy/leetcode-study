class Solution:
    def spiralOrder(self, matrix: List[List[int]]) -> List[int]:
        result = []

        while matrix:
            # 1. 맨 위 행을 통째로 추가
            result += matrix.pop(0)

            # 2. 나머지를 90도 반시계 회전
            matrix = list(zip(*matrix))[::-1]

        return result


